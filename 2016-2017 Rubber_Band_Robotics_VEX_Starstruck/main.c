task autonomous(){
	if(SensorValue(autonSelect) < 1500){

		setDrive(60);

		delay(250);

		setDrive(0);

		delay(100);

		setRightDrive(-50);

		delay(350);

		setRightDrive(0);

		delay(100);

		setDrive(-50);

		delay(750);

		setDrive(0);

		delay(100);

		setLauncher(50);

		delay(350);

		setLauncher(15);

		delay(100);

		setDrive(-127);

		delay(1000);

		setDrive(0);

		delay(100);

		setLeftDrive(-60);

		delay(350);

		setLeftDrive(0);

		delay(100);

		setDrive(-127);
		setLauncher(127);

		delay(750);

		setDrive(0);
		setLauncher(0);
	}
	else if(SensorValue(autonSelect) >= 1500 && SensorValue(autonSelect) <= 2500){

		setDrive(70);
		delay(4000);
		setDrive(0);
	}
	else if(SensorValue(autonSelect) > 2500){

		setDrive(127);
		delay(1000);
		setDrive(-15);
		delay(100);
		setDrive(0);
	}
}

