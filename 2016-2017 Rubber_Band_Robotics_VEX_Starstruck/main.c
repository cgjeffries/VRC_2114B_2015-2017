task usercontrol(){
	int count = 0;
	int number = 1;
	// User control code here, inside the loop
	string mainBattery;
	string expanderBattery;
	string backupBattery;
	string LCDArmPos;
	string LCDClawPos;
  while (true){
  	updateLauncherVelocity();
		driver();
		launcher();
		misc();
		launcherAuto();
		//claw();
		clawAlternate();
		delay(10);
		currentTime++;

		clearLCDLine(0);
		clearLCDLine(1);
		battery = SensorValue[expanderBatt]/280;
		sprintf(LCDArmPos, "%1.2i%c", SensorValue(launcherValue));
		sprintf(mainBattery, "%1.2f%c", nImmediateBatteryLevel/1000.0);
		sprintf(expanderBattery, "%1.2f%c", (float)SensorValue[expanderBatt]/280);
		sprintf(backupBattery, "%1.2f%c", BackupBatteryLevel/1000.0);
		sprintf(LCDClawPos, "%1.2f%c", SensorValue(clawValue));
		displayLCDString(0, 0, "Arm:");
		displayNextLCDString(LCDArmPos);
		displayNextLCDString(" CBrake:");
		if(clawBrake)
			displayNextLCDString("Y");
		else
			displayNextLCDString("N");
		if(number == 1)
		{
			displayLCDString(1, 0, "Main:");
			displayNextLCDString(mainBattery);
		}
		else if(number == 2)
		{
			displayLCDString(1, 0, "Drive:");
			displayNextLCDString(expanderBattery);
		}
		else if(number == 3)
		{
			displayLCDString(1, 0, "Backup:");
			displayNextLCDString(backupBattery);
		}
		count ++;
		if(count == 300)
			number = 2;
		else if(count == 600)
			number = 3;
		else if(count == 900)
		{
			number = 1;
			count = 0;
		}
  }
}
