void setAuton()
{
	int autonTemp = 1;
	while(nLCDButtons != centerButton && bIfiRobotDisabled)
	{
		clearLCDLine(0);
		clearLCDLine(1);
		if(nLCDButtons == leftButton)
		{
			if(autonTemp != 1)
			{
				autonTemp --;
			}
			waitForRelease("leftButton");
			//waitForRelease();
		}
		if(nLCDButtons == rightButton)
		{
			if(autonTemp != 7)
			{
				autonTemp ++;
			}
			waitForRelease("rightButton");
		}
		if(autonTemp == 1)
		{
			auton = "nothing";
		}
		else if(autonTemp == 2)
		{
			auton = "cube+stars";
		}
		else if(autonTemp == 3)
		{
			auton = "cube";
		}
		else if(autonTemp == 4)
		{
			auton = "drive_forward";
		}
		else if(autonTemp == 5)
		{
			auton = "nothing";
		}
		else if(autonTemp == 6)
		{
			auton = "TestDistance";
		}
		else if(autonTemp == 7)
		{
			auton = "TestRotate";
		}
		displayLCDString(0, 0, auton);
		delay(100);
	}
	waitForRelease("centerButton");
}


