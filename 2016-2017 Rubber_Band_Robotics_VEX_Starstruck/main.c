void updateLauncherVelocity(){
	if((nSysTime - previousRealTime) > 100){
		velocity = (((lastLauncherValue - SensorValue(launcherValue)) / (nSysTime - previousRealTime)) * 1000);
		lastLauncherValue = SensorValue(launcherValue);
		previousRealTime = nSysTime;
	}
}

