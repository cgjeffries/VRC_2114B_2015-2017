bool raiseStarOffGround = false;



void setLeftLauncher(int speed){
	motor[leftLauncher1] = speed;
	motor[leftLauncher2] = speed;
	motor[leftLauncher3] = speed;
}

void setRightLauncher(int speed){
	motor[rightLauncher1] = speed;
	motor[rightLauncher2] = speed;
	motor[rightLauncher3] = speed;
}

void setLauncher(int speed){
	setRightLauncher(speed);
	setLeftLauncher(speed);
}

void setLeftDrive(int speed){
	motor[leftDrive1] = speed;
	motor[leftDrive2] = speed;
}

void setRightDrive(int speed){
	motor[rightDrive1] = speed;
	motor[rightDrive2] = speed;
}

void launcherAuto(){
	if(raiseStarOffGround){
		if(SensorValue(launcherValue) > -130){
			setLauncher(60);
		}
		else{
			setLauncher(15);
		}
	}
}

void misc(){
	if(vexRT[Btn8D]){
		raiseStarOffGround = true;
	}
}

void driver(){
	if(abs(vexRT[Ch3]) > 10){
		setLeftDrive(vexRT[Ch3]);
	}
	else{
		setLeftDrive(0);
	}

	if(abs(vexRT[Ch2]) > 10){
		setRightDrive(vexRT[Ch2]);
	}
	else{
		setRightDrive(vexRT[Ch2]);
	}
}

void launcher(){
	if(vexRT[Btn6U]){
		setLauncher(127);
		raiseStarOffGround = false;
	}
	else if(vexRT[Btn6D]){
		setLauncher(-20);
		raiseStarOffGround = false;
	}
	else{
		if(SensorValue(launcherValue) <= -100){
			setLauncher(15);
		}
		else{
			if(!raiseStarOffGround){
				setLauncher(0);
			}
		}
	}
}




