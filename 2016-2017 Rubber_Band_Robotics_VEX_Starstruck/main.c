bool raiseStarOffGround = false;



void setLeftLauncher(int speed){
	motor[leftLauncher1] = speed;
	motor[leftLauncher2] = speed;
	motor[leftLauncher3] = speed;
	motor[leftLauncher4] = speed;
}

void setRightLauncher(int speed){
	motor[rightLauncher1] = speed;
	motor[rightLauncher2] = speed;
	motor[rightLauncher3] = speed;
	motor[rightLauncher4] = speed;
}

void setLauncher(int speed){
	setRightLauncher(speed);
	setLeftLauncher(speed);
}

void setLeftDrive(int speed){
	motor[leftDrive] = speed;
}

void setRightDrive(int speed){
	motor[rightDrive] = speed;
}

void setDrive(int speed){
	setRightDrive(speed);
	setLeftDrive(speed);
}

void launcherAuto(){
	if(raiseStarOffGround){
		if(SensorValue(launcherValue) > -200){
			setLauncher(60);
		}
		else{
			setLauncher(15);
		}
	}
}

void misc(){
	if(vexRT[Btn8D]){
		raiseStarOffGround = true;
	}
}

void driver(){
	if(abs(vexRT[Ch3]) > 10){
		setLeftDrive(vexRT[Ch3]);
	}
	else{
		setLeftDrive(0);
	}

	if(abs(vexRT[Ch2]) > 10){
		setRightDrive(vexRT[Ch2]);
	}
	else{
		setRightDrive(vexRT[Ch2]);
	}
}

void launcher(){
	if(vexRT[Btn6U] && SensorValue(launcherValue) > -500){
		setLauncher(127);
		raiseStarOffGround = false;
	}
	else if(vexRT[Btn6D]){
		setLauncher(-30);
		raiseStarOffGround = false;
	}
	else{
		if(SensorValue(launcherValue) <= -100 && !raiseStarOffGround){
			setLauncher(15);
		}
		else{
			if(!raiseStarOffGround){
				setLauncher(0);
			}
		}
	}
}

void defliporlift(){
	if(vexRT[Btn6U]){
		setLauncher(127);
	}
	else if(vexRT[Btn6D]){
		setLauncher(-127);
	}
	else{
		setLauncher(0);
	}
}

void halfLift(){
	if(vexRT[Btn6U]){
		setLauncher(127);
	}
	else if(vexRT[Btn6D]){
		setLauncher(-60);
	}
	else{
		setLauncher(0);
	}
}




