void waitForRelease(char * button)
{
	int number;
	if(button != "all")
	{
		if(button == "rightButton")
			number = 4;
		else if(button == "leftButton")
			number = 1;
		else if(button == "centerButton")
			number = 2;

		while(nLCDButtons == number)
			delay(5);
	}
	else
	{
		while(nLCDButtons != 0){}
		wait1Msec(5);
	}
}

